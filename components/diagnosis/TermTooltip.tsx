// 증상·상태 수식어 패턴 (이름 끝에 붙는 것들)
const SUFFIX_RE = /\s*(시스템\s*)?(마모|오염|불량|고장|결함|손상|누유|과다|부족|경화|막힘|파손|이상|적체|퇴적|크랙|균열|누출|누수|소음|진동|고착|실패|슬립|오류|통신\s*오류|불량\s*및.+)?$/

function stripSuffix(name: string): string {
  return name.replace(SUFFIX_RE, '').trim()
}

function findDictEntry(name: string): string | null {
  // 1. 정확 일치
  if (TERM_DICT[name]) return TERM_DICT[name]

  // 2. 공백 무시 일치
  const noSpace = name.replace(/\s/g, '')
  for (const key of Object.keys(TERM_DICT)) {
    if (noSpace === key.replace(/\s/g, '')) return TERM_DICT[key]
  }

  // 3. 증상 수식어 제거 후 일치
  const stripped = stripSuffix(name)
  if (stripped && stripped !== name) {
    if (TERM_DICT[stripped]) return TERM_DICT[stripped]
    const strippedNoSpace = stripped.replace(/\s/g, '')
    for (const key of Object.keys(TERM_DICT)) {
      if (strippedNoSpace === key.replace(/\s/g, '')) return TERM_DICT[key]
    }
  }

  // 4. 긴 키가 이름 안에 포함되는지 확인 (부분 일치)
  const keys = Object.keys(TERM_DICT).sort((a, b) => b.length - a.length)
  for (const key of keys) {
    if (key.length >= 3 && name.includes(key)) return TERM_DICT[key]
    if (key.length >= 3 && stripped.includes(key)) return TERM_DICT[key]
  }

  return null
}

interface CauseNameProps {
  name: string
  enName?: string
}

/**
 * 원인 이름 + 탭하면 펼치는 쉬운 설명
 * - 사전 등록 → 즉시 사용 가능 (탭 시 표시)
 * - 미등록 → 첫 탭 시 API 호출
 */
export function CauseNameWithExplain({ name, enName }: CauseNameProps) {
  const dictExplanation = findDictEntry(name)
  const [explanation, setExplanation] = useState<string | null>(dictExplanation)
  const [loading, setLoading] = useState(false)
  const [open, setOpen] = useState(false)
  const [fetched, setFetched] = useState(!!dictExplanation)

  const handleToggle = async () => {
    if (open) { setOpen(false); return }
    setOpen(true)
    // 아직 설명 없고 API 호출 안 했으면 호출
    if (!fetched && !loading) {
      setLoading(true)
      try {
        const cleanTerm = stripSuffix(name) || name
        const res = await fetch('/api/assist', {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({ type: 'term_explain', term: cleanTerm, enName }),
        })
        if (!res.ok) throw new Error(`HTTP ${res.status}`)
        const data = await res.json()
        if (data.answer) setExplanation(data.answer)
      } catch {
        // 실패 시 조용히
      } finally {
        setLoading(false)
        setFetched(true)
      }
    }
  }

  const hasExplanation = !!explanation || loading

  return (
    <div>
      {/* 원인명 텍스트 직접 탭 → 설명 토글 */}
      <div className="flex items-center gap-2 flex-wrap">
        <button
          onClick={handleToggle}
          className={`font-bold text-gray-900 text-sm text-left leading-snug ${hasExplanation ? 'underline decoration-dotted decoration-blue-300 underline-offset-2' : ''}`}
        >
          {name}
        </button>
        {enName && <span className="text-xs text-gray-400">{enName}</span>}
      </div>

      {/* 쉬운 설명 (펼쳤을 때) */}
      {open && (
        loading ? (
          <p className="mt-1 text-[11px] text-blue-400">설명 불러오는 중...</p>
        ) : explanation ? (
          <p className="mt-1.5 text-[11px] text-blue-600 leading-relaxed bg-blue-50 rounded-lg px-2.5 py-1.5">
            {explanation}
          </p>
        ) : null
      )}
    </div>
  )
}
