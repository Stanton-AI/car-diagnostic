-- ── repair_costs 테이블 생성 ─────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS repair_costs (
  id              uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  repair_item     text NOT NULL,          -- "브레이크 패드 교체 (전륜)"
  category        text,                   -- "brake" | "engine" | "tyre" | "electric" ...
  keywords        text[],                 -- 매칭용 키워드 배열
  car_maker       text,                   -- NULL = 공통, "현대", "기아" 등
  car_model       text,                   -- NULL = 공통, "아반떼" 등
  parts_min       integer NOT NULL,
  parts_max       integer NOT NULL,
  labor_min       integer NOT NULL,
  labor_max       integer NOT NULL,
  source          text DEFAULT 'manual',  -- "manual" | "user_feedback"
  sample_count    integer DEFAULT 1,
  last_verified_at timestamptz DEFAULT now(),
  created_at      timestamptz DEFAULT now(),
  updated_at      timestamptz DEFAULT now()
);

CREATE INDEX IF NOT EXISTS repair_costs_category_idx  ON repair_costs(category);
CREATE INDEX IF NOT EXISTS repair_costs_keywords_idx  ON repair_costs USING GIN(keywords);
CREATE INDEX IF NOT EXISTS repair_costs_maker_idx     ON repair_costs(car_maker, car_model);

